#include "libft.h"

void	ft_print_tab_int(int *tab, int size)
{
	int		i;

	i = 0;
	while (size > i)
	{
		ft_putnbr_fd(2, tab[i]);
		ft_putchar_fd(2, '\t');
		i++;
	}
}
